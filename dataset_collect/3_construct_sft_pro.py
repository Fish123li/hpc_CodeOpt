import os
import json
import re
import hashlib
import random
from pathlib import Path
from tqdm import tqdm

# ================= 配置 =================
SOURCE_DIR = r"E:\about_task\HPC_Model_FT\middle_collect\clone_repository"
OUTPUT_TRAIN = r"E:\about_task\HPC_Model_FT\middle_collect\hpc_sft_train.jsonl"
OUTPUT_TEST = r"E:\about_task\HPC_Model_FT\middle_collect\hpc_sft_test.jsonl"

ALLOWED_EXTS = {'.c', '.cpp', '.cc', '.cu', '.cuh', '.f90', '.f', '.h', '.hpp'}
# 敏感信息正则
EMAIL_REGEX = re.compile(r'\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}\b')
IP_REGEX = re.compile(r'\b(?:\d{1,3}\.){3}\d{1,3}\b')

# 专家规则库
RULES = [
    (r'MPI_Init|MPI_Comm_rank', "Implement a distributed memory program using MPI standards."),
    (r'__global__\s+void|cudaMalloc', "Write a CUDA kernel function for GPU acceleration."),
    (r'#pragma\s+omp\s+parallel', "Optimize the following code loop using OpenMP directives."),
    (r'#include\s+<immintrin.h>|_mm256_', "Implement SIMD vectorization using AVX intrinsics."),
    (r'cblas_|dgemm|lapack', "Perform linear algebra operations using BLAS/LAPACK libraries.")
]


def clean_pii(content):
    content = EMAIL_REGEX.sub("<EMAIL>", content)
    return IP_REGEX.sub("<IP_ADDR>", content)


def is_generated_code(content):
    """过滤机器生成的长代码"""
    if "generated by" in content[:500].lower(): return True
    lines = content.split('\n')
    if len(lines) > 0 and (sum(len(l) for l in lines) / len(lines) > 150): return True
    return False


def get_instruction(content, filename):
    for pattern, instruct in RULES:
        if re.search(pattern, content): return instruct
    ext = Path(filename).suffix
    if ext in ['.cu', '.cuh']:
        return "Complete the following CUDA code snippet."
    elif ext in ['.f90', '.f']:
        return "Write a Fortran routine for scientific computation."
    return "Analyze and complete the following High Performance Computing code."


def process():
    print("🛠️  开始构建 SFT 数据集 (Pro版)...")

    # 1. 仓库级划分 (Repo-level Split)
    all_repos = [d for d in Path(SOURCE_DIR).iterdir() if d.is_dir()]
    if not all_repos:
        print("❌ 未找到仓库文件夹，请先运行 Step 2")
        return

    random.seed(42)
    random.shuffle(all_repos)
    split_idx = int(len(all_repos) * 0.9)
    train_repos = set(all_repos[:split_idx])
    test_repos = set(all_repos[split_idx:])

    print(f"📊 划分: 训练仓库 {len(train_repos)} 个, 测试仓库 {len(test_repos)} 个")

    seen_hashes = set()
    train_buffer, test_buffer = [], []

    for repo_dir in tqdm(all_repos, desc="Processing"):
        is_test = repo_dir in test_repos
        for root, dirs, files in os.walk(repo_dir):
            if '.git' in dirs: dirs.remove('.git')

            for file in files:
                file_path = Path(root) / file
                if file_path.suffix.lower() not in ALLOWED_EXTS: continue

                try:
                    if not (200 < file_path.stat().st_size < 200 * 1024): continue  # 大小过滤

                    with open(file_path, 'r', encoding='utf-8', errors='ignore') as f:
                        content = f.read()

                    # 机器代码过滤
                    if is_generated_code(content): continue

                    # 哈希去重
                    h = hashlib.md5(content.replace("\n", "").replace(" ", "").encode()).hexdigest()
                    if h in seen_hashes: continue
                    seen_hashes.add(h)

                    # 清洗与构造
                    content = clean_pii(content)
                    instruction = get_instruction(content, file)

                    entry = {"instruction": instruction, "input": "", "output": content}

                    if is_test:
                        test_buffer.append(entry)
                    else:
                        train_buffer.append(entry)
                except:
                    continue

    # 保存
    with open(OUTPUT_TRAIN, 'w', encoding='utf-8') as f:
        for e in train_buffer: f.write(json.dumps(e, ensure_ascii=False) + "\n")
    with open(OUTPUT_TEST, 'w', encoding='utf-8') as f:
        for e in test_buffer: f.write(json.dumps(e, ensure_ascii=False) + "\n")

    print(f"✅ 完成！训练集: {len(train_buffer)} 条, 测试集: {len(test_buffer)} 条")


if __name__ == "__main__":
    process()